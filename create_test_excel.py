import pandas as pd

# Создаем тестовые данные с числами
# Правильный ответ - это номер варианта (1, 2, 3, 4)
data = {
    'Вопрос': [
        'Какое число больше?',
        'Выберите целое число',
        'Какой результат деления?'
    ],
    'Правильный ответ': [3, 2, 2],  # Индексы правильных ответов
    'Вес': [1, 1, 1],
    'Вариант 1': [4.0, 1.0, 1.0],
    'Вариант 2': [5.0, 2.0, 1.5],  # Правильные ответы
    'Вариант 3': [6.0, 2.5, 2.0],  # 6.0 - правильный для первого вопроса
    'Вариант 4': [3.5, 3.0, 2.5]
}

df = pd.DataFrame(data)
df.to_excel('test_numbers.xlsx', index=False)
print('Excel файл создан успешно!')
print(df)